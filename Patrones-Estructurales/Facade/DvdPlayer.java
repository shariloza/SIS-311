public class DvdPlayer {
    public void on() { System.out.println("Reproductor DVD: Encendido"); }
    public void play(String movie) { System.out.println("Reproductor DVD: Reproduciendo \"" + movie + "\""); }
    public void stop() { System.out.println("Reproductor DVD: Detenido"); }
    public void off() { System.out.println("Reproductor DVD: Apagado"); }
}
